package SecondDayOdev.ReCapDemoClasses;

public class DortIslem {

    public int topla(int n1, int n2) {
        return n1 + n2;
    }
    public int cikar(int n1, int n2) {
        return n1 - n2;
    }
    public int carp(int n1, int n2) {
        return n1 * n2;
    }
    public int bol(int n1, int n2) {
        return n1 / n2;
    }

}
